package paquete.clases;

/**
 *
 * @author Leonardo
 * @param <E>
 */
public class Nodo <E> {
    private E info;
    private Nodo<E> siguiente;

    public Nodo() {
        this.info=null;
        this.siguiente=null;
    }

    public E getInfo() {
        return info;
    }

    public void setInfo(E info) {
        this.info = info;
    }

    public Nodo<E> getSiguiente() {
        return siguiente;
    }

    public void setSiguiente(Nodo<E> siguiente) {
        this.siguiente = siguiente;
    }
}
